a=30
b=2
#Arithmetic Operators- Simple Calculations
sum=a+b
diff=a-b
multiply=a*b
div=a/b
mod=a%b #remainder
power=a**b #a^b

#Relational Operators- Compare
print(sum, diff, multiply, div,mod,power)
print(a==b)
print(a!=b)
print(a>b)
print(a<b)

#Assignment Operators- assign the values
a+=a #adds the initial value of a to the a itself
print(a)

#Logical Operators- combine or invert boolean expressions, returning a value of TRUE or FALSE
a=50
b=20
print(not(a>b))
